public class Buku {
    public String judul;
    public String pengarang;
    public String penerbit;
    public int tahun;
    public int category;

    public Buku(String judul, String pengarang, String penerbit, int tahun, int category) {
        this.judul = judul;
        this.pengarang = pengarang;
        this.penerbit = penerbit;
        this.tahun = tahun;
        this.category = category;
    }
}
